/// Thin wrapper over the `lumi/app_icon` MethodChannel (AppIconChannel.swift).
///
/// iOS itself persists the applied icon across launches, so there is no local
/// state to store — the current selection is always read back from the system.
class AppIconService {
  static const MethodChannel _channel = MethodChannel('lumi/app_icon');

  /// Cheap synchronous check used to hide entry points on platforms that can
  /// never switch icons. [isSupported] asks iOS for the definitive answer.
  static bool get isSupportedPlatform =>
      !kIsWeb && defaultTargetPlatform == TargetPlatform.iOS;

  Future<bool> isSupported() async {
    if (!isSupportedPlatform) return false;
    return await _channel.invokeMethod<bool>('isSupported') ?? false;
  }

  /// The active icon, resolved against [kLumiAppIcons]; falls back to the
  /// default entry if iOS reports an icon name the catalog no longer contains.
  Future<LumiAppIcon> currentIcon() async {
    final name = await _channel.invokeMethod<String>('getAlternateIconName');
    return kLumiAppIcons.firstWhere(
      (icon) => icon.iosIconName == name,
      orElse: () => kLumiAppIcons.first,
    );
  }

  /// Applies [icon]. iOS shows its own confirmation alert on success; throws
  /// a [PlatformException] on failure (e.g. unknown icon name).
  Future<void> setIcon(LumiAppIcon icon) {
    return _channel.invokeMethod<void>(
      'setAlternateIconName',
      {'iconName': icon.iosIconName},
    );
  }
}
