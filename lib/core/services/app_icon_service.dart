/// One entry in the Lumi app-icon pack.
class LumiAppIcon {
  /// Stable identifier; also the preview asset's basename.
  final String id;

  /// Name shown in the picker.
  final String displayName;

  /// Alternate-icon set name in Assets.xcassets, or null for the primary icon.
  final String? iosIconName;

  const LumiAppIcon({
    required this.id,
    required this.displayName,
    this.iosIconName,
  });

  String get previewAsset => 'assets/app_icons/$id.png';

  bool get isDefault => iosIconName == null;
}

